  // Restituisce l'SVG dell'icona. Se il nome non esiste, stringa vuota.
  function get(name) {
    return ICONS[name] || "";
  }
  const EMOJI_MAP = {
    "!": "bell",
    "D": "moon",
    "L": "sun",
    "Q": "search",
  };

  // Sostituisce i marcatori dentro il dropdown utente con l'SVG giusto.
  // Targettato solo su .uh-dd-item per non toccare contenuto user-generated.
  function replaceHeaderEmojis() {
    const items = document.querySelectorAll(".uh-dd-item span[aria-hidden='true']");
    items.forEach((sp) => {
      const txt = (sp.textContent || "").trim();
      const firstCh = Array.from(txt)[0];
      if (firstCh && EMOJI_MAP[firstCh]) {
        sp.innerHTML = `<span class="nm-ico" aria-hidden="true">${get(EMOJI_MAP[firstCh])}</span>`;
      } else if (txt && EMOJI_MAP[txt]) {
        sp.innerHTML = `<span class="nm-ico" aria-hidden="true">${get(EMOJI_MAP[txt])}</span>`;
      }
    });
    const themeBtn = document.getElementById("themeBtn");
    if (themeBtn && !themeBtn.querySelector("svg")) {
      const cur = document.documentElement.getAttribute("data-theme") || "dark";
      themeBtn.innerHTML = `<span class="nm-ico" aria-hidden="true">${get(cur === "dark" ? "sun" : "moon")}</span>`;
    }
    // Icona lente nella barra di ricerca dell'header.
    const searchIco = document.querySelector(".uh-search .uh-search-ico");
    if (searchIco && !searchIco.querySelector("svg")) {
      searchIco.innerHTML = get("search");
    }
  }

  // Espongo su window cosi' le altre pagine possono usare le icone
  window.NMIcons = {
    get,
    replaceHeaderEmojis,
    ICONS,
    EMOJI_MAP,
  };

  // Auto-run al load. Se poi renderHeaderUser ridisegna il dropdown,
  // core.js dispatcha 'nm:headerUpdated' e riapplico.
  function boot() {
    replaceHeaderEmojis();
    if (typeof window !== "undefined") {
      window.addEventListener("nm:headerUpdated", replaceHeaderEmojis);
    }
  }
  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", boot);
  } else {
    boot();
  }
